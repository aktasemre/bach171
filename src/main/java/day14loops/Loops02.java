package day14loops;

public class Loops02 {

    public static void main(String[] args) {
        int sum = 0;
        for (int i = 5; i <9 ; i++) {

            sum=sum+i ;
        }
        System.out.println("sum = " + sum);
        System.out.println("--------------");
       int multiplay=1;
        for (int i = 7; i <10 ; i++) {

            multiplay*=i;
        }
        System.out.println(multiplay);
    }

}
