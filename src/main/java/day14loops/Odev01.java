package day14loops;

public class Odev01 {
    public static void main(String[] args) {

        for (int i = 1; i <11 ; i++) {
            for (int j = 1; j <11 ; j++) {
               int carp = i*j;
                System.out.println(i +"x"+j+"="+carp);
            }
            System.out.println();

        }


    }

}
